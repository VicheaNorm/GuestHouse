    private void CheckInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckInActionPerformed
        try {
            
            String name=txtFirstName.getText()+" "+txtLastName.getText();
            String sex=cmbGender.getSelectedItem().toString();
            String address=txtAddress.getText()+" " +cmbCountry.getSelectedItem().toString();
            String passNum=txtPassport.getText();
            //insert into customer
            pst=data.getConnectio().prepareStatement(SqlConfigString.INSERT_ONE_INTO_GEUST_BYCHECKIN);
            pst.setString(1, name);
            pst.setString(2, sex);
            pst.setString(3, address);
            pst.setString(4, passNum);
            pst.execute();
            pst.close();
            //getCusID
            int cusNum=0;
            pst=data.getConnectio().prepareStatement(SqlConfigString.SELECT_LASTID_CUST);
            res=pst.executeQuery();
            while(res.next()){cusNum=res.getInt(1);}
            //insert into checkIn
            pst=data.getConnectio().prepareStatement(SqlConfigString.INSERT_INTO_CHECKIN);
            pst.setInt(1,1);
            pst.setInt(2,bookNum);
            pst.setInt(3,cusNum);
            pst.execute();
            pst.close();
            //get CheckIn Id
            int checkIn_id=0;
            pst=data.getConnectio().prepareStatement(SqlConfigString.SELECT_CHECKIN_ID);
            res=pst.executeQuery();
            while(res.next()){
                checkIn_id=res.getInt(1);
            }
            res.close();
            //insert into CheckIn detail
            //get value to filled in tblcheck_in_detail
            String d1,d2;
            d1=frm.format(DateIn.getDate());
            d2=frm.format(DateOut.getDate());
            int adult,child;
            adult=Integer.parseInt(cmbAdult.getSelectedItem().toString());
            child=Integer.parseInt(cmbChild.getSelectedItem().toString());
            pst=data.getConnectio().prepareStatement(SqlConfigString.INSERT_INTO_CHECKIN_DETAIL);
            pst.setInt(1,checkIn_id);
            pst.setInt(2,roomNum);
            pst.setString(3, d1);
            pst.setString(4, d2);
            pst.setInt(5,adult);
            pst.setInt(6,child);
            pst.setFloat(7, pay);
            pst.setFloat(8, balance);
            pst.execute();
            pst.close();
            //Update Room Status
            pst=data.getConnectio().prepareStatement(SqlConfigString.UPDATE_ROOM);
            pst.setInt(1,3);
            pst.setInt(2,roomNum);
            pst.execute();
            pst.close();
            //Update status in tblBook
            pst=data.getConnectio().prepareStatement(SqlConfigString.UPDATE_ROOM_INBOOKDETAIL);
            pst.setString(1,"Check In");
            pst.setInt(2,roomNum);
            pst.execute();
            pst.close();
        } catch (SQLException ex) {
            Logger.getLogger(frmCheckIn.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frmCheckIn.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InstantiationException ex) {
            Logger.getLogger(frmCheckIn.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalAccessException ex) {
            Logger.getLogger(frmCheckIn.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this, "Check In Successfully");
     
        
    }//GEN-LAST:event_CheckInActionPerformed

