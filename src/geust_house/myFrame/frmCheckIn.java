    private void DateOutPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_DateOutPropertyChange
      
      if("date".equalsIgnoreCase(evt.getPropertyName())){
          try {
              JDateChooser dateOut=(JDateChooser) evt.getSource();
              if(DateIn.getDate().compareTo(dateOut.getDate())>0){
                  JOptionPane.showMessageDialog(this,"Check Out Date Cannot Before CheckIn Date");
              }
              //get periodDays of Staying
              int diff=(int) ((dateOut.getDate().getTime()-DateIn.getDate().getTime())/(1000*3600*24));
              txtPeriod.setText(diff+" days");
              try {
                  pst= data.getConnectio().prepareStatement(SqlConfigString.SELECT_ROOMPRICE);
              } catch (ClassNotFoundException ex) {
                  Logger.getLogger(frmCheckIn.class.getName()).log(Level.SEVERE, null, ex);
              } catch (InstantiationException ex) {
                  Logger.getLogger(frmCheckIn.class.getName()).log(Level.SEVERE, null, ex);
              } catch (IllegalAccessException ex) {
                  Logger.getLogger(frmCheckIn.class.getName()).log(Level.SEVERE, null, ex);
              }
              //get RoomID
              roomNum=Integer.parseInt(cmbRoom.getSelectedItem().toString());
              pst.setInt(1,roomNum);
              pst.setInt(2,6);
              res=pst.executeQuery();
              //set Values to text Of TotalCharge
              while(res.next()){
                  
                  txtTotalCharge.setText(df.format(diff*res.getInt(1)));
                  txtOtherCharge.setText(df.format(0));
                  txtSubTotal.setText(df.format(diff*res.getInt(1)));
                  total=diff*res.getInt(1);
              }
              txtTotal.setText(df.format(total-(total*dis)));           
          } catch (SQLException ex) {
              Logger.getLogger(frmCheckIn.class.getName()).log(Level.SEVERE, null, ex);
          }
      }
    }//GEN-LAST:event_DateOutPropertyChange

