    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        for(int i =0;i<tblProduct.getRowCount();i++){
            try {
                //get Item from tblProduct
                int id=Integer.parseInt(tblProduct.getValueAt(i,0).toString());
                String date=df.format(c.getTime());
                int qty_import=Integer.parseInt(tblProduct.getValueAt(i,2).toString());
                float price=Float.parseFloat(tblProduct.getValueAt(i,3).toString());
                int qty_instock = 0;
                float cost=0;
                //get total qauntity of the product in the stock
                pst=data.getConnectio().prepareStatement(SqlConfigString.STOCK_BALANCING);
                pst.setInt(1, id);
                res=pst.executeQuery();
                while(res.next()){
                    qty_instock=res.getInt(1);
                }
                res.close();
                //get the previuos cost of product
                pst=data.getConnectio().prepareStatement(SqlConfigString.SELECT_COSTOFPRODUCT_TOUPDATE);
                pst.setInt(1, id);
                res=pst.executeQuery();
                while(res.next()){
                    cost=res.getFloat(1);
                }
                 res.close();
                //insert into product+transaction
                pst=data.getConnectio().prepareStatement(SqlConfigString.INSERT_INTO_PRO_TRANSACTION);
                pst.setInt(1,id);
                pst.setString(2,date);
                pst.setInt(3,qty_import);
                pst.setFloat(4,price);
                pst.setInt(5,1);
                pst.setInt(6,2);
                pst.execute();
                pst.close();
               
                
                //update cost of product
                pst=data.getConnectio().prepareStatement(SqlConfigString.UPDATE_PRODUCT_COST);
                float rCost=(qty_instock*cost+qty_import*price)/(qty_import+qty_instock);
                pst.setFloat(1,rCost);
                pst.setInt(2, id);
                pst.execute();
                pst.close();
                
                 this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(frmImport.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(frmImport.class.getName()).log(Level.SEVERE, null, ex);
            } catch (InstantiationException ex) {
                Logger.getLogger(frmImport.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IllegalAccessException ex) {
                Logger.getLogger(frmImport.class.getName()).log(Level.SEVERE, null, ex);
            }
             JOptionPane.showMessageDialog(this,"Import Product is Successfully");
    }
    }//GEN-LAST:event_btnOkActionPerformed

