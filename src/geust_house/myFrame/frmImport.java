    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        //Check if the Value is invalid
        if(txtQty.getText().equals("")&&txtPrice.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please Input quantity and price of product!!!");
        }
        else if(txtQty.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please Input quantity of product!!!");
        }
        else if(txtPrice.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please Input Price of product!!!");
        }
        else{
            try {
                int qty=Integer.parseInt(txtQty.getText());
                float price=Float.parseFloat(txtPrice.getText());
                float amount=qty*price;
                float t=0;
                String pName=cmbProduct.getSelectedItem().toString();
                pst=data.getConnectio().prepareStatement(SqlConfigString.SELECT_PRODUCT_BYNAME);
                pst.setString(1, pName);
                res=pst.executeQuery();
                boolean check=false;
                txtQty.setText("");
                txtPrice.setText("");
                while(res.next()){
                    //init the first row to tblProduct
                    if(tblProduct.getRowCount()==0){
                            m.addRow(new Object[]{
                        res.getInt(1),pName,qty,price,amount
                    });
                            
                    }
                    else {
                        for(int i=0;i<tblProduct.getRowCount();i++){
                            //Update value of product that has the same name
                            if(cmbProduct.getSelectedItem().toString().equals(tblProduct.getValueAt(i,1))){
                                m.setValueAt(qty,i,2);
                                m.setValueAt(price, i,3);
                                m.setValueAt(amount, i,4);
                                check=true;
                                
                            }
                        }
                        if(check==false){
                            //add new row 
                           m.addRow(new Object[]{
                        res.getInt(1),pName,qty,price,amount
                    }); 
                        }
                    }
                     
                }
                res.close();
                //sum total price of all product
                for(int i=0;i<tblProduct.getRowCount();i++){
                     t+=Float.parseFloat((tblProduct.getValueAt(i,4).toString()));
                }
               //set value to txtTotal 
                txtTotal.setText(t+"");
               
            } catch (SQLException ex) {
                Logger.getLogger(frmImport.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(frmImport.class.getName()).log(Level.SEVERE, null, ex);
            } catch (InstantiationException ex) {
                Logger.getLogger(frmImport.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IllegalAccessException ex) {
                Logger.getLogger(frmImport.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }

    }//GEN-LAST:event_btnAddActionPerformed
